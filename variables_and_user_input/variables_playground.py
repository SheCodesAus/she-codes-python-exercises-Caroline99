day = "Saturday"
print(type(day))

# f for formatted screen and curly braces brings in the variable
message = f"Today is {day}!"
print(message)

# Integer
height = 168
print(type(height))

# Float
weight = 66.6
print(type(weight))

# Integers
# Run distance in m
run1_dist = 1400
run2_dist = 1800

# Addition
total_distance = run1_dist + run2_dist
print(total_distance)

# Floats
# Run distance in km
run3_dist = 1.7
run4_dist = 1.35

# Addition
total_distance = run3_dist + run4_dist
print(total_distance)

# Division and Multiplication
# Division always produce floats
print(run1_dist / 1000)
# Integer division
print(run1_dist // 1000)
print(run4_dist * 1000)

